// Operando que atingem um único elemento

let num1 = 1;
let num2 = 2;

num1++;
console.log(num1);
--num1;
console.log(num1);

console.log(++num1 === num2--); // primeiro incrementa, depois compara, e por ultimo subtrai
console.log(num1 === num2);

