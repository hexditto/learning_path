# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer número
# Positivo ou Negativo. int sem sinal -> positivo

print(11) #int
print(-11) #int
print(0) #int

#float -> Número com ponto flutuante
# O tipo float representa qualquer número
# Positivo ou negativo
# Sem sinal -> positivo

print(1.1, 10.11) #float
print(0.12, -1.5) #float
print(0.13) #float

# A função type retorna o tipo
print(type(0.1))
print(type(0))
print(type('Otávio'))
